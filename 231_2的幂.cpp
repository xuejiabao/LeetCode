/* 

给定一个整数，编写一个函数来判断它是否是 2 的幂次方。

常用位运算
1.判断奇偶
  (x & 1) == 1 ---等价---> (x % 2 == 1)
  (x & 1) == 0 ---等价---> (x % 2 == 0)
2.x / 2 ---等价---> x >> 1
3.x &= (x - 1) ------> 把x最低位的二进制1给去掉
4.x & -x -----> 得到最低位的1
5.x & ~x -----> 0

*/

class Solution {
public:
	bool isPowerOfTwo(int n) {
		if (n <= 0)
		{
			return false;
		}
		return n & (n - 1) == 0;
	}
};
